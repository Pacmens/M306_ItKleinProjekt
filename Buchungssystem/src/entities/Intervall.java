package entities;

public class Intervall {
	private Integer intervallId;
	private Integer tage;
	private String name;

	public void setIntervallId(Integer intervallId) {
		this. intervallId = intervallId;
	}

	public Integer getIntervallId() {
		return intervallId;
	}

	public void setTage(Integer tage) {
		this. tage = tage;
	}

	public Integer getTage() {
		return tage;
	}

	public void setName(String name) {
		this. name = name;
	}

	public String getName() {
		return name;
	}

}